#!/bin/bash
# Generate a personalized onboarding script for a new team member.
# Run this BEFORE provision-user.sh — the generated script walks the new
# user through generating their SSH key so they can share it with you.
#
# Usage:
#   ./control/bin/generate-invite.sh <username> <index>
#
#   username  — their Unix username (e.g. bobby)
#   index     — their slot in docs/host-registry.md (1-9)
#
# Output:
#   onboard/<username>-setup.sh  — send this file to the new user
set -euo pipefail
ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
. "$ROOT/config.env"

USERNAME="${1:-}"
INDEX="${2:-}"

if [[ -z "$USERNAME" || -z "$INDEX" ]]; then
  echo "usage: $(basename "$0") <username> <index>" >&2
  exit 1
fi

if ! [[ "$INDEX" =~ ^[1-9]$ ]]; then
  echo "index must be 1–9 (got: $INDEX)" >&2
  exit 1
fi

LOOPBACK_IP="127.0.0.${INDEX}"
STUDIO_HOST="${USERNAME}.studio"
OUT_DIR="$ROOT/onboard"
OUT="$OUT_DIR/${USERNAME}-setup.sh"

mkdir -p "$OUT_DIR"

# Write the self-contained onboarding script for the new user.
# It has no dependencies — just bash and standard macOS tools.
cat > "$OUT" <<SCRIPT
#!/bin/bash
# Team Mac onboarding for ${USERNAME}
# Run this once in your terminal to get set up.
# Generated by your admin — do not share this file publicly.
set -euo pipefail

REMOTE_USER="${USERNAME}"
REMOTE_HOST="${REMOTE_HOST}"
LOOPBACK_IP="${LOOPBACK_IP}"
STUDIO_HOST="${STUDIO_HOST}"
SSH_ALIAS="${ALIAS_NAME:-box}"

# ── helpers ──────────────────────────────────────────────────────────────────
_bold=\$(tput bold 2>/dev/null || true)
_green=\$(tput setaf 2 2>/dev/null || true)
_cyan=\$(tput setaf 6 2>/dev/null || true)
_yellow=\$(tput setaf 3 2>/dev/null || true)
_reset=\$(tput sgr0 2>/dev/null || true)

banner() { echo ""; echo "\${_bold}\${_cyan}\$*\${_reset}"; echo ""; }
ok()     { echo "\${_green}✓\${_reset}  \$*"; }
note()   { echo "\${_yellow}→\${_reset}  \$*"; }
hr()     { printf '%0.s─' \$(seq 1 60); echo; }

clear
hr
echo "\${_bold}  Welcome, ${USERNAME}! Let's get you set up on the team Mac.\${_reset}"
hr
echo ""
echo "  This script takes about 2 minutes."
echo "  Just follow the prompts — no technical knowledge needed."
echo ""
read -r -p "  Press Enter to begin… "

# ── step 1: SSH key ───────────────────────────────────────────────────────────
banner "Step 1 of 3 — Create your access key"

KEY_FILE=""
if [[ -f "\$HOME/.ssh/id_ed25519.pub" ]]; then
  KEY_FILE="\$HOME/.ssh/id_ed25519"
  ok "You already have an SSH key — we'll use it."
elif [[ -f "\$HOME/.ssh/id_rsa.pub" ]]; then
  KEY_FILE="\$HOME/.ssh/id_rsa"
  ok "You already have an SSH key — we'll use it."
else
  note "Generating a new SSH key for you…"
  mkdir -p "\$HOME/.ssh" && chmod 700 "\$HOME/.ssh"
  ssh-keygen -t ed25519 -f "\$HOME/.ssh/id_ed25519" -N "" -C "${USERNAME}@team" -q
  KEY_FILE="\$HOME/.ssh/id_ed25519"
  ok "SSH key created."
fi

PUBKEY="\$(cat "\${KEY_FILE}.pub")"

# Copy to clipboard silently so they can paste it
echo "\$PUBKEY" | pbcopy 2>/dev/null || true

echo ""
hr
echo "\${_bold}  Your public key (already copied to your clipboard):\${_reset}"
echo ""
echo "\${_cyan}\${PUBKEY}\${_reset}"
hr
echo ""
echo "  \${_bold}Send this key to your admin\${_reset} — paste it in Slack, email, or however"
echo "  you usually message them. It's safe to share."
echo ""
echo "  Once you've sent it, wait for them to confirm you're all set."
echo ""
read -r -p "  Press Enter once your admin says you're ready… "

# ── step 2: test the connection ───────────────────────────────────────────────
banner "Step 2 of 3 — Connect to the Mac"

note "Testing your connection…"
if ! ssh -o BatchMode=yes -o ConnectTimeout=10 \
        -o StrictHostKeyChecking=accept-new \
        -i "\${KEY_FILE}" \
        "\${REMOTE_USER}@\${REMOTE_HOST}" 'echo ok' &>/dev/null; then
  echo ""
  echo "  ⚠️  Couldn't connect. A few things to check:"
  echo ""
  echo "     • Did your admin confirm they've added you? (They run a command on their end.)"
  echo "     • Are you on the same network or VPN as the team Mac?"
  echo "     • Make sure you sent the key shown above — the whole line."
  echo ""
  echo "  Ask your admin to double-check, then run this script again."
  exit 1
fi
ok "Connection works!"

# ── step 3: configure your machine ───────────────────────────────────────────
banner "Step 3 of 3 — Configure your Mac"

# SSH config — add a Host entry so you can type 'ssh box' or use the alias
SSH_CONFIG="\$HOME/.ssh/config"
touch "\$SSH_CONFIG" && chmod 600 "\$SSH_CONFIG"

if grep -q "Host \${SSH_ALIAS}\$" "\$SSH_CONFIG" 2>/dev/null; then
  ok "SSH shortcut already configured."
else
  cat >> "\$SSH_CONFIG" <<SSH_ENTRY

# Team Mac — added by agent-mac-ops onboarding
Host \${SSH_ALIAS}
    HostName \${REMOTE_HOST}
    User \${REMOTE_USER}
    IdentityFile \${KEY_FILE}
    ServerAliveInterval 60
SSH_ENTRY
  ok "SSH shortcut '\${SSH_ALIAS}' added."
fi

# /etc/hosts — so your browser can reach your.studio:3000
if grep -qF "\${STUDIO_HOST}" /etc/hosts 2>/dev/null; then
  ok "/etc/hosts already has \${STUDIO_HOST}."
else
  note "Adding \${STUDIO_HOST} to /etc/hosts (will ask for your Mac password)…"
  echo "\${LOOPBACK_IP}  \${STUDIO_HOST}" | sudo tee -a /etc/hosts > /dev/null
  ok "\${STUDIO_HOST} → \${LOOPBACK_IP} added."
fi

# ── done ─────────────────────────────────────────────────────────────────────
echo ""
hr
echo "\${_bold}\${_green}  All done! You're set up.\${_reset}"
hr
echo ""
echo "  \${_bold}Connect to the team Mac:\${_reset}"
echo ""
echo "      ssh \${SSH_ALIAS}"
echo ""
echo "  \${_bold}Your dev server URLs (once you start them):\${_reset}"
echo ""
echo "      http://\${STUDIO_HOST}:3000   ← web"
echo "      http://\${STUDIO_HOST}:8080   ← API"
echo ""
echo "  To forward those URLs to your laptop while connected:"
echo ""
echo "      ssh -L 3000:\${LOOPBACK_IP}:3000 -L 8080:\${LOOPBACK_IP}:8080 \${SSH_ALIAS}"
echo ""
hr
echo ""
SCRIPT

chmod +x "$OUT"

echo ""
echo "✅  Invite script generated: onboard/${USERNAME}-setup.sh"
echo ""
echo "   Send that file to $USERNAME and ask them to run:"
echo ""
echo "       bash ~/Downloads/${USERNAME}-setup.sh"
echo ""
echo "   They'll generate their SSH key and share it with you."
echo "   Once you have it, provision them:"
echo ""
echo "       ./control/bin/provision-user.sh $USERNAME $INDEX"
echo ""
echo "   Then tell them to press Enter in the setup script."
echo ""
