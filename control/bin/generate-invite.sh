#!/bin/bash
# Generate a personalized onboarding script for a new team member, then
# upload it to a private GitHub Gist and print a single curl command to
# send them. No file transfer needed.
#
# Usage:
#   ./control/bin/generate-invite.sh <username> <index>
#
# Requires: gh CLI + gh auth login (for Gist upload)
# Fallback: prints the file path for manual transfer if gh isn't available
set -euo pipefail
ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
. "$ROOT/config.env"

USERNAME="${1:-}"
INDEX="${2:-}"

if [[ -z "$USERNAME" || -z "$INDEX" ]]; then
  echo "usage: $(basename "$0") <username> <index>" >&2
  exit 1
fi

if ! [[ "$INDEX" =~ ^[1-9]$ ]]; then
  echo "index must be 1–9 (got: $INDEX)" >&2
  exit 1
fi

LOOPBACK_IP="127.0.0.${INDEX}"
STUDIO_HOST="${USERNAME}.studio"
OUT_DIR="$ROOT/onboard"
OUT="$OUT_DIR/${USERNAME}-setup.sh"

mkdir -p "$OUT_DIR"

# ── write the self-contained onboarding script ────────────────────────────────
cat > "$OUT" <<SCRIPT
#!/bin/bash
# Team Mac onboarding for ${USERNAME}
# Run this once in your terminal to get set up.
# Generated by your admin — do not share this link publicly.
set -euo pipefail

REMOTE_USER="${USERNAME}"
REMOTE_HOST="${REMOTE_HOST}"
LOOPBACK_IP="${LOOPBACK_IP}"
STUDIO_HOST="${STUDIO_HOST}"
SSH_ALIAS="${ALIAS_NAME:-box}"

# ── helpers ───────────────────────────────────────────────────────────────────
_bold=\$(tput bold 2>/dev/null || true)
_green=\$(tput setaf 2 2>/dev/null || true)
_cyan=\$(tput setaf 6 2>/dev/null || true)
_yellow=\$(tput setaf 3 2>/dev/null || true)
_reset=\$(tput sgr0 2>/dev/null || true)

banner() { echo ""; echo "\${_bold}\${_cyan}\$*\${_reset}"; echo ""; }
ok()     { echo "\${_green}✓\${_reset}  \$*"; }
note()   { echo "\${_yellow}→\${_reset}  \$*"; }
hr()     { printf '%0.s─' \$(seq 1 60); echo; }

clear
hr
echo "\${_bold}  Welcome, ${USERNAME}! Let's get you set up on the team Mac.\${_reset}"
hr
echo ""
echo "  This takes about 2 minutes. Just follow the prompts."
echo ""
read -r -p "  Press Enter to begin… "

# ── step 1: SSH key ───────────────────────────────────────────────────────────
banner "Step 1 of 3 — Create your access key"

KEY_FILE=""
if [[ -f "\$HOME/.ssh/id_ed25519.pub" ]]; then
  KEY_FILE="\$HOME/.ssh/id_ed25519"
  ok "You already have an SSH key — we'll use it."
elif [[ -f "\$HOME/.ssh/id_rsa.pub" ]]; then
  KEY_FILE="\$HOME/.ssh/id_rsa"
  ok "You already have an SSH key — we'll use it."
else
  note "Generating a new SSH key for you…"
  mkdir -p "\$HOME/.ssh" && chmod 700 "\$HOME/.ssh"
  ssh-keygen -t ed25519 -f "\$HOME/.ssh/id_ed25519" -N "" -C "${USERNAME}@team" -q
  KEY_FILE="\$HOME/.ssh/id_ed25519"
  ok "SSH key created."
fi

PUBKEY="\$(cat "\${KEY_FILE}.pub")"
echo "\$PUBKEY" | pbcopy 2>/dev/null || true

echo ""
hr
echo "\${_bold}  Your public key (already copied to your clipboard):\${_reset}"
echo ""
echo "\${_cyan}\${PUBKEY}\${_reset}"
hr
echo ""
echo "  \${_bold}Send this to your admin\${_reset} — paste it in Slack or however you message them."
echo "  It's safe to share. Wait for them to say you're all set."
echo ""
read -r -p "  Press Enter once your admin confirms you're ready… "

# ── step 2: test the connection ───────────────────────────────────────────────
banner "Step 2 of 3 — Verify connection"

note "Testing your connection…"
if ! ssh -o BatchMode=yes -o ConnectTimeout=10 \
        -o StrictHostKeyChecking=accept-new \
        -i "\${KEY_FILE}" \
        "\${REMOTE_USER}@\${REMOTE_HOST}" 'echo ok' &>/dev/null; then
  echo ""
  echo "  ⚠️  Couldn't connect. Things to check:"
  echo ""
  echo "     • Did your admin confirm they've added you?"
  echo "     • Are you on the same VPN or network as the team Mac?"
  echo "     • Make sure you sent the full key line shown above."
  echo ""
  echo "  Ask your admin to double-check, then run this again."
  exit 1
fi
ok "Connection works!"

# ── step 3: configure your machine ───────────────────────────────────────────
banner "Step 3 of 3 — Configure your Mac"

SSH_CONFIG="\$HOME/.ssh/config"
touch "\$SSH_CONFIG" && chmod 600 "\$SSH_CONFIG"

if grep -q "Host \${SSH_ALIAS}\$" "\$SSH_CONFIG" 2>/dev/null; then
  ok "SSH shortcut already set up."
else
  cat >> "\$SSH_CONFIG" <<SSH_ENTRY

# Team Mac — added by agent-mac-ops onboarding
Host \${SSH_ALIAS}
    HostName \${REMOTE_HOST}
    User \${REMOTE_USER}
    IdentityFile \${KEY_FILE}
    ServerAliveInterval 60
SSH_ENTRY
  ok "SSH shortcut '\${SSH_ALIAS}' added."
fi

if grep -qF "\${STUDIO_HOST}" /etc/hosts 2>/dev/null; then
  ok "/etc/hosts already has \${STUDIO_HOST}."
else
  note "Adding \${STUDIO_HOST} to /etc/hosts (will ask for your Mac password)…"
  echo "\${LOOPBACK_IP}  \${STUDIO_HOST}" | sudo tee -a /etc/hosts > /dev/null
  ok "\${STUDIO_HOST} added."
fi

# ── done ─────────────────────────────────────────────────────────────────────
echo ""
hr
echo "\${_bold}\${_green}  You're all set!\${_reset}"
hr
echo ""
echo "  \${_bold}Connect to the team Mac:\${_reset}         ssh \${SSH_ALIAS}"
echo ""
echo "  \${_bold}Your dev server (once running):\${_reset}  http://\${STUDIO_HOST}:3000"
echo "                                    http://\${STUDIO_HOST}:8080"
echo ""
echo "  To view dev servers in your local browser while connected:"
echo "    ssh -L 3000:\${LOOPBACK_IP}:3000 -L 8080:\${LOOPBACK_IP}:8080 \${SSH_ALIAS}"
echo ""
hr
echo ""
SCRIPT

chmod +x "$OUT"

# ── upload to a private Gist → one curl command for the new user ─────────────
CURL_CMD=""
if command -v gh &>/dev/null && gh auth status &>/dev/null 2>&1; then
  GIST_FILENAME="${USERNAME}-setup.sh"
  GIST_URL=$(gh gist create \
    --filename "$GIST_FILENAME" \
    --desc "Team Mac onboarding for ${USERNAME}" \
    "$OUT" 2>/dev/null || true)
  if [[ -n "$GIST_URL" ]]; then
    # https://gist.github.com/USER/ID → https://gist.githubusercontent.com/USER/ID/raw/FILENAME
    RAW_URL="${GIST_URL/gist.github.com/gist.githubusercontent.com}/raw/${GIST_FILENAME}"
    CURL_CMD="curl -fsSL ${RAW_URL} | bash"
  fi
fi

echo ""
if [[ -n "$CURL_CMD" ]]; then
  echo "✅  Send ${USERNAME} this one line (paste it in Slack):"
  echo ""
  echo "    ${CURL_CMD}"
  echo ""
  echo "   They run it → get their SSH key → send it to you."
  echo "   Then: ./users provision ${USERNAME}"
else
  echo "✅  Invite script: onboard/${USERNAME}-setup.sh"
  echo ""
  echo "   Tip: run 'gh auth login' to get a shareable one-liner next time."
  echo ""
  echo "   Send ${USERNAME} that file and ask them to run:"
  echo "     bash ~/Downloads/${USERNAME}-setup.sh"
  echo ""
  echo "   Then: ./users provision ${USERNAME}"
fi
echo ""
